<template>
  <div class="list">
    <Loading v-if="loading"></Loading>
    <div
      class="item"
      v-for="(item, index) in list"
      :key="index"
      @click="goDetail(item.filmId)"
    >
      <div class="left">
        <img :src="item.poster" />
      </div>
      <div class="middle">
        <div>{{ item.name }}</div>
        <span>{{ item.filmType.name }}</span>
        <div>
          <span>上映时间 </span>
          <span class="label">{{ item.premiereAt | parsePremiereAt }}</span>
        </div>
        <div>主演：{{ item.actors | parseActors }}</div>
        <div v-if="type == 1">{{ item.nation }} | {{ item.runtime }}分钟</div>
      </div>
      <div class="right">
        <span v-if="type == 1">购票</span>
        <span v-else>预约</span>
      </div>
    </div>
  </div>
</template>
<script>
// 导入组件
import Loading from "./loading";
import moment from "moment";
export default {
  data() {
    return {
      // 设置默认显示loading
      loading: true,
    };
  },
  //子组件从父组件拿数据
  props: ["list", "type"],
  created() {
    // 判断是否有数据，如果有数据就移除loading加载层
    if (this.list.length > 0) {
      this.loading = false;
    }
  },
  components: {
    Loading,
  },
  filters: {
    //处理演员的数据，将主演的数据从数组形式转化成字符串形式以便输出
    parseActors: function (value) {
      let actors = "";
      value.forEach((element) => {
        actors += element.name + " ";
      });
      return actors;
    },
    parsePremiereAt: function (value) {
      // 时间戳的单位是秒，需要乘以1000转化成毫秒进行处理
      return moment(value * 1000).format("YYYY-MM-DD");
    },
  },
  methods: {
    goDetail: function (filmId) {
      this.$router.push({ name: "detail", params: { filmId } });
    },
  },
};
</script>
